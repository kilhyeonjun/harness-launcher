#!/usr/bin/env bash
# codex-migrate-to-symlinks.sh — convert legacy/hand-edited .harness/codex/
# layouts into the canonical view that codex-home-prepare.sh produces.
#
# The Claude harness is the single source of truth. The Codex layer is
# entirely derived: AGENTS.md/skills/* are symlinks back to .claude/ sources;
# config.toml/hooks.json/agents/*.toml are regenerated from those sources.
# Any real file under .harness/codex/ that does not match the generator's
# fingerprint is treated as stale and backed up before regeneration.
#
# Usage: codex-migrate-to-symlinks.sh <harness-dir>

set -euo pipefail

HARNESS_DIR="${1:?HARNESS_DIR required (positional arg 1)}"
[[ -d "$HARNESS_DIR" ]] || { echo "harness dir not found: $HARNESS_DIR" >&2; exit 1; }

CODEX_HOME="$HARNESS_DIR/.harness/codex"
[[ -d "$CODEX_HOME" ]] || { echo "✓ no .harness/codex/ to migrate"; exit 0; }

needs=0

# Stale config.toml — anything without our generator header is not ours
if [[ -f "$CODEX_HOME/config.toml" && ! -L "$CODEX_HOME/config.toml" ]]; then
  if ! head -1 "$CODEX_HOME/config.toml" 2>/dev/null \
       | grep -q "Generated by codex-home-prepare.sh"; then
    needs=1
  fi
fi

# Skill entries that are real directories (not symlinks) indicate a hand-copied
# layout from before v0.4.0's per-skill symlink merge.
if [[ -d "$CODEX_HOME/skills" ]]; then
  for entry in "$CODEX_HOME/skills"/*; do
    [[ -e "$entry" ]] || continue
    [[ "$(basename "$entry")" == ".system" ]] && continue
    if [[ -d "$entry" && ! -L "$entry" ]]; then
      needs=1
      break
    fi
  done
fi

if [[ "$needs" -eq 0 ]]; then
  echo "✓ .harness/codex/ already in canonical layout — nothing to migrate"
  exit 0
fi

ts=$(date +%Y%m%d-%H%M%S)
backup="$HARNESS_DIR/.harness/codex.backup-$ts"
mv "$CODEX_HOME" "$backup"
echo "→ Backed up existing .harness/codex/ to .harness/codex.backup-$ts"

prep="$(cd "$(dirname "$0")" && pwd)/codex-home-prepare.sh"
[[ -x "$prep" ]] || {
  echo "FATAL: codex-home-prepare.sh not found next to migration script: $prep" >&2
  exit 1
}
"$prep" "$HARNESS_DIR"
echo "✓ Regenerated canonical .harness/codex/. Inspect $backup for any user customizations."
